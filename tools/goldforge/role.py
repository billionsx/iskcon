#!/usr/bin/env python3
"""
ЗКН-П016 · ЗАКОН НЕОБХОДИМОСТИ ФАКТА.

Прежний сборщик был СВАЛЬЩИКОМ: брал каждый стих, где мелькнуло имя, и клал в
список по книгам и главам. Получалась не книга о личности, а помойка — сто
цитат, из которых девяносто не говорят о герое ничего.

Факт входит в карточку, только если известно ДВА:
    1. ЧТО ОН ДАЁТ  — событие · труд · учение · слава · связь
    2. КУДА ОН ИДЁТ — раздел карточки определяется РОЛЬЮ, а не книгой

РОЛИ (и куда попадает пассаж):

    актор        герой ДЕЙСТВУЕТ или ГОВОРИТ          → Житие (эпизод)
    труд         речь о его книге                     → Труды
    авторитет    на него ССЫЛАЮТСЯ как на прамана     → Учение и авторитет
    качество     его славят, описывают качества       → Прославление
    перечисление имя в ряду других имён               → Связи (ссылка, не цитата)
    упоминание   имя мелькнуло — и ничего            → БРИФ. В карточку НЕ идёт

Последнее — главное. «Упоминание» не выбрасывается (ЗКН-П009), оно уходит
куратору. Но в книгу о личности оно не входит: факт, который ничего не даёт,
не имеет права занимать место.
"""
import re
import unicodedata


def fold(s):
    s = (s or "").lower().replace("ё", "е")
    s = unicodedata.normalize("NFD", s)
    return "".join(c for c in s if not unicodedata.combining(c))


# Герой ДЕЙСТВУЕТ: пришёл, сказал, написал, поклонился, основал…
ACTION = re.compile(
    r"\b(сказал|говорил|ответил|спросил|воскликнул|произнес|обратил|молил|"
    r"пришел|прибыл|отправил|вернул|встретил|увидел|привел|принял|отверг|"
    r"написал|составил|создал|сложил|перевел|издал|основал|учредил|"
    r"проповедовал|учил|наставлял|объяснил|рассказал|поведал|открыл|"
    r"поклонил|припал|обнял|заплакал|воспел|восславил|запел|танцевал|"
    r"остал|посели|жил|ушел|покинул|родил|явил|яви́л|умер|оставил|"
    r"попросил|велел|приказал|дал|вручил|получил|принес|показал|повел|"
    r"служил|поклонялся|совершал|исполнял|读)")

# На героя ССЫЛАЮТСЯ как на авторитет: «как пишет X», «согласно X»
AUTHORITY = re.compile(
    r"(как\s+(пишет|писал|говорит|утверждает|объясняет|отмечает|указывает|"
    r"замечает|подтверждает)|по\s+словам|согласно|со\s+ссылкой\s+на|"
    r"цитиру|ссыла|приводит\s+слова|в\s+сво(ей|ем)\s+(книге|труде|комментарии)|"
    r"в\s+комментарии|как\s+сказано\s+у|подтверждает)")

# Его СЛАВЯТ, описывают качества.
# Маркер обязан быть ОДНОЗНАЧНЫМ. «дорог» ловил «денег на дорогу» и уводил
# эпизод про поездку в раздел «Прославление». Широкий маркер — ложь на выходе.
GLORY = re.compile(
    r"\b(слав[аыуеоий]|славит|прославл|милост|качеств|велич|возвышенн|"
    r"непревзойденн|лотосным стопам|поклоны|дандават|поклон[яе]|"
    r"чистый преданн|нет равных|образец)")

CAPNAME = re.compile(r"(?<![А-Яа-яЁё])[А-ЯЁ][а-яё]{2,}")
# Список имён = имена ЧЕРЕЗ ЗАПЯТУЮ: «Рупа, Санатана, Джива, Гопала Бхатта…».
# Считать перечислением всякую фразу с тремя заглавными — значит объявить
# списком обычное повествование («Между Бхактиведантой Свами и организаторами
# из Японии…»): «Свами» — часть его же имени, «Японии» — страна.
ROSTER = re.compile(r"(?:[А-ЯЁ][а-яё]+(?:\s+[А-ЯЁ][а-яё]+)?\s*,\s*){2,}")
NEAR = 90          # окно вокруг имени, в котором ищем маркер роли

IN_CARD = ("актор", "труд", "авторитет", "качество")
TO_LINKS = ("перечисление",)
TO_BRIEF = ("упоминание",)


def _windows(text, pat):
    """Окна вокруг каждого вхождения имени."""
    f = fold(text)
    for m in pat.finditer(f):
        lo, hi = max(0, m.start() - NEAR), min(len(f), m.end() + NEAR)
        yield f[lo:hi], m.start(), m.end()


def _sentence(text, pos):
    lo = max(text.rfind(". ", 0, pos), text.rfind("\n", 0, pos)) + 1
    hi = text.find(". ", pos)
    hi = hi + 1 if hi != -1 else len(text)
    return text[lo:hi]


def classify(text, name_pat, works=()):
    """Роль героя в пассаже. Решает и вход в карточку, и раздел.

    Порядок проверок не случаен: «действие» сильнее «упоминания», а ссылка на
    авторитет («как пишет Джива Госвами») — НЕ житие, сколько бы глаголов вокруг
    ни стояло: действует там не герой, а тот, кто на него ссылается.
    """
    best = "упоминание"
    for win, start, _end in _windows(text, name_pat):
        # 1. на героя ссылаются как на источник знания
        before = win[:NEAR + 4]
        if AUTHORITY.search(before):
            return "авторитет"
        # 2. речь о его труде
        for w in works:
            if fold(w) in win:
                return "труд"
        # 3. герой действует или говорит
        if ACTION.search(win):
            best = "актор"
            continue
        # 4. его славят
        if GLORY.search(win) and best != "актор":
            best = "качество"
            continue
        # 5. имя в ряду других имён — это связь, не эпизод
        if best == "упоминание":
            sent = _sentence(text, start)
            # Заглавная в НАЧАЛЕ фразы — не имя («Между…», «Но…»). Считать её
            # именем значит объявить перечислением обычное повествование.
            if ROSTER.search(sent) and not ACTION.search(fold(sent)):
                best = "перечисление"
    return best


def gives(role, hero):
    """Что этот факт ДАЁТ. Пусто — значит не даёт ничего и в карточку не идёт."""
    return {
        "актор": "событие жития",
        "труд": "труд героя",
        "авторитет": "его слово как авторитет",
        "качество": "прославление",
        "перечисление": "связь",
    }.get(role, "")
