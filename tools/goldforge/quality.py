#!/usr/bin/env python3
"""
ЗКН-П021 · ЦИТАТА — НЕ СТЕНА, А ВЫСКАЗЫВАНИЕ.

Карточка Шрилы Прабхупады показала, чего стоил мой «исчерпывающий» подход:
раздел «Его миссия — своими словами» открывался стеной пурпорта в 1 500 знаков,
оборванной посреди санскритского слова, — и к миссии не имевшей отношения.
Потому что «лучшие» цитаты я выбирал СОРТИРОВКОЙ ПО ДЛИНЕ. Самое длинное =
самое лучшее. Это идиотизм в одной строке.

Цитата имеет право стоять в книге, только если она:
  • ВЫСКАЗЫВАНИЕ — есть подлежащее и сказуемое, а не обрывок списка имён
  • ЦЕЛАЯ       — начинается с заглавной, кончается точкой. Никаких «....»
  • ЧИТАЕМАЯ    — не слипшийся текст импорта («пран̣аш́йати«Для того…»)
  • НЕ КАНЦЕЛЯРИЯ — «ПШП президентам храмов, подписано…» — это шапка письма
  • КОРОТКАЯ    — человек читает биографию, а не переписывает библиотеку
"""
import re

MAX_PURPORT = 600      # выдержка из комментария: мысль, а не глава
MAX_VERSE = 900        # стих/куплет целиком
MIN_WORDS = 8

# Глагол в личной форме — признак высказывания, а не списка.
VERB = re.compile(r"\b[а-яё]{2,}(ет|ёт|ют|ут|ит|ат|ят|ал|ял|ил|ел|ла|ло|ли|ся|"
                  r"ется|ются|ться|тся|ешь|ете|ем|им|ают|яют|уют)\b", re.I)
# Начало: заглавная, кавычка или тире. Не «…», не строчная, не цифра.
GOOD_START = re.compile(r"^[«\"„А-ЯЁA-Z—]")
GOOD_END = re.compile(r"[.!?»\"“]$")
# Слипшийся импорт: нет пробела между словом и кавычкой или между точкой и словом.
GLUED = re.compile(r"[а-яёa-z][«\"„]|[.»\"][А-ЯЁA-Z]")
# Канцелярия: шапки писем, служебные записки, редакторские пометы.
META = re.compile(r"^\s*(ПШП|ПШ|Письмо|Записка|Телеграмма|Меморандум|Приложение|"
                  r"Комментарий редактора|Примечание)\b|подписано\s+Шрил|"
                  r"просмотренн\w+\s+Шрил|\bГИТА-ПРЕСС\b", re.I)
# Обрывок списка имён: одни имена и запятые.
NAME_LIST = re.compile(r"^[А-ЯЁ][А-Яа-яЁё.\s]+(,\s*[А-ЯЁ][А-Яа-яЁё.\s]+){1,}\.?$")


def clean(t):
    t = re.sub(r"[ \t]+", " ", (t or "")).strip()
    t = re.sub(r"\.{3,}$", "", t).strip()       # хвостовые «....» — не многоточие
    return t


def sentences(t):
    return [s.strip() for s in re.split(r"(?<=[.!?»])\s+", t) if s.strip()]


def excerpt(text, pat, limit):
    """Выдержка ЦЕЛЫМИ предложениями вокруг имени. Слово не режем никогда."""
    t = clean(text)
    if not t:
        return ""
    para = t
    for p in t.split("\n"):
        if p.strip() and pat.search(p.lower()):
            para = p.strip()
            break
    if len(para) <= limit:
        return para
    ss = sentences(para)
    hit = next((i for i, s in enumerate(ss) if pat.search(s.lower())), 0)
    out, lo, hi = [ss[hit]], hit - 1, hit + 1
    while len(" ".join(out)) < limit and (lo >= 0 or hi < len(ss)):
        if hi < len(ss) and len(" ".join(out + [ss[hi]])) <= limit:
            out.append(ss[hi])
            hi += 1
        elif lo >= 0 and len(" ".join([ss[lo]] + out)) <= limit:
            out.insert(0, ss[lo])
            lo -= 1
        else:
            break
    return " ".join(out)


def worthy(t):
    """Имеет ли эта цитата право стоять в книге? Вернёт причину отказа."""
    t = (t or "").strip()
    if len(t.split()) < MIN_WORDS:
        return "обрывок (%d слов)" % len(t.split())
    if META.search(t):
        return "канцелярия (шапка письма, не речь)"
    if NAME_LIST.match(t):
        return "список имён, а не высказывание"
    if not VERB.search(t):
        return "нет сказуемого — это не высказывание"
    if not GOOD_START.match(t):
        return "начинается с обрывка"
    if not GOOD_END.search(t):
        return "оборвана на полуслове"
    if GLUED.search(t):
        return "слипшийся текст импорта (нет пробела)"
    return None


def rank(f):
    """Чем цитата ЛУЧШЕ. Не длиной — краткостью и цельностью.

    Раньше здесь стояло `-len(text)`: выбери самое длинное. Так стена пурпорта
    и попала в раздел «Его миссия — своими словами».
    """
    role_w = {"качество": 0, "авторитет": 1, "труд": 2, "актор": 3}.get(f.get("role"), 4)
    n = len(f.get("text", ""))
    fit = abs(n - 320)                 # ближе к 320 знакам — лучше читается
    return (role_w, fit)
