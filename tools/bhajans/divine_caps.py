# -*- coding: utf-8 -*-
"""Капитализация имён божеств в названиях бхаджан и аудио (стандарт BBT/приложения).

Капитализируется первая буква ТОЛЬКО у токенов из выверенного множества (имена и
эпитеты Бога, святые места, «Шри», праславие джай/джайа/джая). Сопоставление — по
ЦЕЛОМУ токену между пробелами и дефисами, поэтому грамматические слова (ке, на,
прана, мора, крипа, кумара, ване, дайите…) остаются строчными, а подстрочных
коллизий нет (кори≠хори, гаураба≠гаура, парама≠рама).

Применяется в bhajan_loader.py (prayers.name) и fill_recordings.py
(prayer_media.title), чтобы капитализация держалась при повторном импорте источника.
"""

_DEITY = set("""
радха радхе радхика радхарани радхике кришна кришнер кришне мадхава мадхаве
говинда говиндам гопал гопала гопи гопинатха гопинатх нанда нандана шьям шьяма
гаура гауранга гауракишора гауракишор чайтанья чаитанйа чайтанйа нитай нитаи
нитьянанда нимай хари хори рамана рагхава рагхупати кешава мукунда джаганнатха
джаганнатх нараяна дамодара канхайа канхейа мохана вриндаван вриндавана яшомати
яшода гириварадхари шри говардхана
""".split())
_PRAISE = set("джай джайа джая".split())
_CAP = _DEITY | _PRAISE


def _cap_token(tok: str) -> str:
    core = tok.strip("()!?.,'’«»\"—-").lower()
    if core in _CAP and tok[:1].islower():
        for i, ch in enumerate(tok):
            if ch.isalpha():
                return tok[:i] + ch.upper() + tok[i + 1:]
    return tok


def capitalize_divine(name: str) -> str:
    if not name:
        return name
    out = []
    for word in name.split(" "):
        if "-" in word:
            out.append("-".join(_cap_token(t) for t in word.split("-")))
        else:
            out.append(_cap_token(word))
    return " ".join(out)


if __name__ == "__main__":
    for s in ["Джайа радха мадхава", "Джай говинда джай гопал", "Ванде кришна нанда кумара",
              "Радха-кришна гити", "Парама каруна", "Кабе гаура-ване, сурадхуни-тате"]:
        print(f"{s!r} -> {capitalize_divine(s)!r}")
