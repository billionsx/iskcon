def S(eid, t):  return {"id": eid, "t": t}
def sec(h=None, p=None, cite=None, see=None):
    o={}
    if h is not None: o["h"]=h
    if p is not None: o["p"]=p
    if cite is not None: o["cite"]=cite
    if see is not None: o["see"]=see
    return o
def rail(title, params, orderIds=None):
    o={"title":title,"params":params}
    if orderIds is not None: o["orderIds"]=orderIds
    return o
def sub(sid,label,sections=None,rails=None):
    o={"id":sid,"label":label,"sections":sections or []}
    if rails: o["rails"]=rails
    return o
def tab(tid,label,kicker,title,lead,subtabs):
    return {"id":tid,"label":label,"kicker":kicker,"title":title,"lead":lead,"subtabs":subtabs}

