import { RECIPES, OFFERING_PRAYERS, CLASSICS, type Category } from "./prasad";

export type Block =
  | { type: "h"; text: string }                       // подзаголовок
  | { type: "p"; text: string }                       // абзац
  | { type: "ul"; items: string[] }                   // маркированный список
  | { type: "dl"; items: { t: string; d: string }[] } // термин → описание
  | { type: "note"; text: string };                   // выделенная врезка (курсив писания)

export interface Chapter {
  id: string;            // url-сегмент
  part: string;          // надзаголовок-рубрика
  number?: string;       // номер для разделов рецептов
  title: string;
  subtitle?: string;
  blocks?: Block[];      // прозаическая глава
  recipesOf?: Category;  // глава-раздел рецептов: тянет RECIPES этой категории
  prayers?: boolean;     // глава подношения: рендерит OFFERING_PRAYERS
}

export interface Cookbook {
  title: string;
  iast: string;
  subtitle: string;
  author: string;
  blurb: string;
  chapters: Chapter[];
}

