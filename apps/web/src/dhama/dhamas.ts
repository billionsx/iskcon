export interface Person { name: string; q: string; entityId?: string }

export interface Tirtha {
  id: string;
  dhama: DhamaId;
  cluster: string;
  name: string;
  iast?: string;
  kind: TirthaKind;
  lat: number | null;
  lng: number | null;
  blurb: string;
  about: string;
  lila?: string;
  persons?: Person[];
  maps?: string;     // переопределение поискового запроса для карт
  source?: string;
  hero_image?: string;           // hero-фото (vrajapedia / рехост archive.org)
  gallery?: string[];            // дополнительные фото
  sources?: TirthaSource[];      // структурированные описания из священных книг
}

/** Описание места из священной книги: автор, книга, абзацы прозы, сноски. */
export interface TirthaSource {
  author?: string;
  book?: string;
  paragraphs: string[];
  footnotes?: { n?: string; text: string }[];
}

export interface Cluster { id: string; title: string; note?: string }

export interface Dhama {
  id: DhamaId;
  name: string;
  iast: string;
  tagline: string;
  deity: string;
  region: string;
  hero?: string;     // фото (если нет — градиентный hero)
  accent: string;    // hex акцента (тинт kind/градиент)
  center: { lat: number; lng: number; zoom: number };
  intro: string[];
  facts: { k: string; v: string }[];
  clusters: Cluster[];
  tirthas: Tirtha[];
}

