/**
 * dhamas — каталог святых дхам и их тиртх (святых мест).
 *
 * Три дхамы Гаудия-вайшнавской традиции:
 *   • Вриндаван (Враджа-мандала) — земля игр Шри Кришны;
 *   • Навадвипа (девять островов) — место явления и игр Шри Чайтаньи;
 *   • Нилачала (Джаганнатха-Пури) — обитель Господа Джаганнатхи.
 *
 * Источники ассоциаций (место → лила → личность) — традиционные парикрамы
 * (Враджа-мандала-парикрама, «Навадвипа-дхама-махатмья» Бхактивиноды Тхакура,
 * «Чайтанья-чаритамрита»). Описания — оригинальные, не цитаты из защищённых
 * путеводителей. Координаты приблизительны и служат ориентиром на карте;
 * кнопка «Открыть в картах» использует поиск по названию для точности.
 *
 * Личности подаются как чипы: на тапе резолвятся через /api/entities?q=…
 * и открывают страницу героя (graceful, без жёсткой привязки к slug).
 */

export type DhamaId = "vrindavan" | "navadvipa" | "nilachala";

export type TirthaKind =
  | "temple"   // храм / мандир
  | "kunda"    // кунда / саровара (священный водоём)
  | "ghat"     // гхат (ступени к реке)
  | "forest"   // ван / роща
  | "hill"     // холм / парвата
  | "samadhi"  // самадхи / бхаджан-кутир
  | "island"   // двипа (остров)
  | "village"  // грама / селение
  | "river"    // река / тиртха у воды
  | "place";   // прочее святое место

