/**
 * ЗКН-Н001 (историю пишет только этот модуль) · ЗКН-Н002 · ЗКН-Н003 («назад» не выкидывает).
 * nav — единый, единственный на приложение слой управления историей браузера.
 *
 * Проблема, которую он решает: раньше историю писали в двух местах (глобальный
 * роутер App и внутрикнижный роутер BookDetailPage), каждый со своим счётчиком
 * на основе `history.length`. Но `history.length` НЕ уменьшается при back() —
 * поэтому «есть ли куда возвращаться» определялось неверно, и кнопка «назад»
 * вылетала из приложения или вела не туда.
 *
 * Решение (стандарт Apple — навигационный стек): глубина приложения хранится в
 * `history.state.appIdx` и обновляется на каждом popstate. Любой переход «вперёд»
 * увеличивает appIdx (push); замена записи (стих↔стих) appIdx не меняет (replace);
 * возврат читает appIdx целевой записи. `canGoBack()` = appIdx > 0 — это надёжный
 * критерий «под нами есть запись приложения», в отличие от history.length.
 *
 * ВАЖНО: ВСЕ pushState/replaceState приложения должны идти ТОЛЬКО через эти
 * функции, иначе appIdx в history.state потеряется.
 */

let idx = 0;

/** Прочитать/проставить appIdx у текущей (корневой) записи. Вызывать один раз при старте. */
export function navInit(): void {
  if (typeof window === "undefined") return;
  const st = window.history.state as { appIdx?: number } | null;
  if (st && typeof st.appIdx === "number") {
    idx = st.appIdx;
  } else {
    idx = 0;
    try { window.history.replaceState({ ...(st || {}), appIdx: 0 }, ""); } catch { /* noop */ }
  }
}

/** Текущая глубина приложения. */
export function navIdx(): number { return idx; }

/** Синхронизировать счётчик из state записи, на которую перешёл браузер (popstate). */
export function navSetIdxFromState(state: unknown): void {
  const s = state as { appIdx?: number } | null;
  idx = s && typeof s.appIdx === "number" ? s.appIdx : 0;
}

/* ─────────────────────────────────────────────────────────────────────────
 * ЗКН-Н039 — АДРЕС МЕНЯЕТСЯ → РОУТЕР ЗНАЕТ. ВСЕГДА. ОДНИМ ПУТЁМ.
 *
 * КОРЕНЬ БЕЛЫХ ЭКРАНОВ И «ПЕРЕЕЗЖАЮЩИХ ПОДТАБОВ».
 *
 * Было: `notifyNav` звался ТОЛЬКО из popstate. Значит роутер и подписчики
 * узнавали о смене адреса ТОЛЬКО когда человек жал «назад». А `pushUrl`
 * из вкладки/зала/книги менял адрес МОЛЧА — экран и адрес расходились:
 *
 *   • Кришна/«Гуна» → жмёшь «Гауранга» → адрес стал /gauranga, но EntityPage
 *     об этом не уведомили: он читал СТАРЫЙ путь и восстанавливал «Гуну»
 *     уже в Гауранге. Человек видел бред.
 *   • Зал писал pushUrl("/kirtans"), состояние App не пересчитывалось —
 *     а «назад» на тот же адрес пересчитывало. Отсюда «через раз».
 *
 * Теперь запись истории — ОДНА ЦЕПОЧКА, одинаковая и для клика, и для «назад»:
 *
 *      история  →  роутер (применить путь)  →  подписчики (подтабы, залы)
 *
 * Порядок детерминирован. Второго пути нет.
 * ───────────────────────────────────────────────────────────────────────── */
type Router = (path: string) => void;
let router: Router | null = null;

/** Регистрирует App как роутер приложения. Вызывается один раз. */
export function setRouter(fn: Router): void { router = fn; }

/** Путь без хеша и без хвостового слэша — то, что понимает роутер. */
function pathOf(url: string): string {
  const p = (url.split("#")[0] || "/").split("?")[0] || "/";
  return p.startsWith("/") ? p : "/" + p;
}

/** Применить путь: роутер, затем подписчики. Единственная точка. */
export function applyRoute(path: string): void {
  const clean = pathOf(path);
  if (router) router(clean);
  notifyNav(clean);
}

/** Новый уровень навигации (новая запись истории). Экран МЕНЯЕТСЯ → роутер применяет путь. */
export function pushUrl(url: string): void {
  if (typeof window === "undefined") return;
  idx += 1;
  try { window.history.pushState({ appIdx: idx }, "", url); } catch { /* noop */ }
  applyRoute(url);
}

/* Замена текущей записи (тот же уровень: стих↔стих, подтаб, нормализация адреса).
 * appIdx не меняется. Экран УЖЕ верный — его поставил тот, кто зовёт replace,
 * поэтому роутер не трогаем (иначе он сбросил бы оверлей, который сам и рисуется).
 * Но подписчиков будим: они обязаны видеть свежий адрес. */
export function replaceUrl(url: string): void {
  if (typeof window === "undefined") return;
  try { window.history.replaceState({ appIdx: idx }, "", url); } catch { /* noop */ }
  notifyNav(pathOf(url));
}

/** Есть ли под нами запись приложения, на которую можно вернуться через history.back(). */
export function canGoBack(): boolean { return idx > 0; }


/* ─────────────────────────────────────────────────────────────────────────
 * ЗКН-Н002 — ОДИН ГЛОБАЛЬНЫЙ popstate НА ПРИЛОЖЕНИЕ.
 *
 * Второй слушатель = гонка истории: App и компонент реагируют на одно
 * событие, а порядок не гарантирован. Так внутрикнижная навигация могла
 * отработать раньше маршрутизатора и оставить экран в противоречивом виде.
 *
 * Владелец `popstate` — App. Все остальные ПОДПИСЫВАЮТСЯ: App разбирает
 * маршрут и только потом оповещает подписчиков — порядок детерминирован.
 * ───────────────────────────────────────────────────────────────────────── */
type NavSub = (path: string) => void;
const subs = new Set<NavSub>();

/** Подписка на навигацию. Возвращает функцию отписки. */
export function subscribeNav(fn: NavSub): () => void {
  subs.add(fn);
  return () => { subs.delete(fn); };
}

/** Вызывает App ПОСЛЕ разбора маршрута. Никто больше. */
export function notifyNav(path: string): void {
  for (const fn of subs) {
    try { fn(path); } catch { /* подписчик не должен ронять навигацию */ }
  }
}
