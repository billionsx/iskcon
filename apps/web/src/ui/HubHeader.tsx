/**
 * ЗКН-Н024 — ШАПКА ВИТРИНЫ: ОДИН КОМПОНЕНТ НА ВСЕ БОГАТСТВА.
 *
 * ПОЧЕМУ ЭТОТ ФАЙЛ СУЩЕСТВУЕТ.
 *
 * Каждая витрина рисовала шапку сама, и они разъехались:
 *
 *   Личности   отступ 6   надпись через тернарник   описание есть
 *   Книги      отступ 4   «Библиотека»              описание есть
 *   Киртаны    отступ 8   надпись переменной        описания НЕТ
 *   Прасад     отступа нет   надписи нет            описания НЕТ
 *   Дхама      отступа нет   «Святые места»         описания НЕТ
 *
 * Пять витрин — пять разных шапок. Человек переключает вкладку, и текст
 * прыгает. Это не мелочь: прыжок разрушает ощущение единого приложения.
 *
 * ЛЕЧИТСЯ ТОЛЬКО ОБЩИМ КОМПОНЕНТОМ. Пока каждая витрина рисует шапку сама,
 * любая договорённость об отступах разъедется снова — через месяц и без злого
 * умысла. Гейт запрещает рисовать шапку в обход этого файла.
 *
 * ТРИ СЛОЯ, И КАЖДЫЙ ОБЯЗАТЕЛЕН:
 *
 *   НАДПИСЬ (золотом)  — КОНТЕКСТ. Чем этот раздел является.
 *                        НЕ повторяет вкладку: вкладка «Личности» → надпись
 *                        «Вечные спутники», а не «Личности» ещё раз.
 *   ЗАГОЛОВОК          — ИМЯ раздела.
 *   ОПИСАНИЕ           — ОРИЕНТИР. Что человек здесь найдёт. Обязательно:
 *                        витрина без описания оставляет гостя в догадках.
 */
import { useRef, type ReactNode } from "react";
import { SearchField } from "./ios";
import { tk } from "./tokens";

export function HubHeader({ eyebrow, title, subtitle, action, level = 1 }: {
  /** Надпись золотом — КОНТЕКСТ раздела. Не повторяет вкладку. */
  eyebrow: string;
  /** Имя раздела. */
  title: string;
  /** Ориентир: что здесь найдётся. Обязателен для шапки ЭКРАНА. */
  subtitle?: string;
  /** Действие справа (например «Весь каталог»). */
  action?: ReactNode;
  /** 1 — шапка ЭКРАНА (h1). 2 — шапка раздела ВНУТРИ экрана (h2). */
  level?: 1 | 2;
}) {
  const H = level === 1 ? "h1" : "h2";
  return (
    /* ЗКН-Н024 — ПОДЗАГОЛОВОК ВСЕГДА ВО ВСЮ ШИРИНУ.
     *
     * Здесь стоял ОДИН flex-ряд: [надзаголовок · заголовок · подзаголовок] [кнопка].
     * Кнопка «Весь каталог» (≈150px) отжимала текстовую колонку — и подзаголовок
     * Бхаджанов ломался в узкий обрубок в три строки, тогда как в остальных
     * витринах (где кнопки нет) он шёл во всю ширину. Одна и та же шапка выглядела
     * по-разному — ровно то, что ЗКН-Н024 и запрещает.
     *
     * Кнопка живёт в ряду с ЗАГОЛОВКОМ. Подзаголовок — строкой ниже, на всю ширину. */
    <div style={{ marginBottom: 14 }}>
      <div style={{ display: "flex", alignItems: "flex-start", justifyContent: "space-between", gap: 10 }}>
        <div style={{ minWidth: 0 }}>
          <div style={{
            fontFamily: 'var(--font-text)', fontSize: 'var(--text-caption2)', lineHeight: 'var(--lh-caption2)', letterSpacing: 'var(--ls-caption2)', fontWeight: 600,
            textTransform: "uppercase", color: "var(--color-gold-deep)",
          }}>{eyebrow}</div>

          <H style={{
            margin: "2px 0 0",
            // Роль целиком. Трекинг был −0.4px на обоих уровнях; канон §3.2 даёт
            // Title 1 **+0.36 pt** — разрядку, а не поджим. Знак был обратным.
            ...(level === 1 ? tk.type.title1 : tk.type.title2),
            fontWeight: 800, color: "var(--color-label)",
          }}>{title}</H>
        </div>

        {action ? <div style={{ flexShrink: 0, paddingTop: 4 }}>{action}</div> : null}
      </div>

      {subtitle ? (
        <p style={{
          margin: "4px 0 0", fontFamily: 'var(--font-text)', fontSize: 'var(--text-subhead)', lineHeight: 'var(--lh-subhead)', letterSpacing: 'var(--ls-subhead)',
          color: "var(--color-label-2)",
        }}>{subtitle}</p>
      ) : null}
    </div>
  );
}

/**
 * ЗКН-Н044 — ПОИСК ВИТРИНЫ: ОДИН КОМПОНЕНТ НА ВСЕ БОГАТСТВА.
 *
 * ПОЧЕМУ ЭТОТ КОМПОНЕНТ ЖИВЁТ РЯДОМ С ШАПКОЙ.
 *
 * Шапку общим компонентом мы уже свели (ЗКН-Н024) — а поиск оставили каждому.
 * И он разъехался ровно так же, только хуже: разъехалось само НАЛИЧИЕ.
 *
 *   Книги      лупа · очистка · Enter → первый · рамка-волосок  ← эталон
 *   Бхаджаны   голый input: ни лупы, ни очистки, радиус 12, кегль другой
 *   Дхама      капсула fill-1 высотой 40: ни рамки, ни клавиатуры
 *   Личности   поиска НЕТ (хотя 706 героев и ResultRow лежит мёртвым кодом)
 *   Киртаны    поиска НЕТ
 *   Прасад     поиска НЕТ
 *
 * Человек переключает вкладку — и поле то есть, то нет, то другое. Витрина без
 * поиска — это витрина, в которой нельзя ничего найти: 706 личностей, 178
 * бхаджанов, сотни рецептов лежат за перелистыванием.
 *
 * ЛЕЧИТСЯ ТОЛЬКО ОБЩИМ КОМПОНЕНТОМ — как и шапка. Пока каждая витрина рисует
 * поле сама, любая договорённость разъедется снова. Гейт запрещает рисовать
 * строку поиска в обход этого файла.
 *
 * ЭТАЛОН — КНИГИ: лупа слева · крестик очистки справа · Esc сбрасывает ·
 * Enter открывает первый результат · нативная клавиатура поиска.
 */
export function HubSearch({ value, onChange, placeholder, ariaLabel, onSubmit }: {
  value: string;
  onChange: (v: string) => void;
  /** Что ищем — словами витрины: «Поиск книги, автора или санскрита». */
  placeholder: string;
  ariaLabel?: string;
  /** Enter → открыть первый результат. Нет результата — ничего не делаем. */
  onSubmit?: () => void;
}) {
  const ref = useRef<HTMLInputElement | null>(null);
  return (
    /* ЗКН-Д019 — ЭТАЛОН APP STORE, ОДНА РЕАЛИЗАЦИЯ НА ПРИЛОЖЕНИЕ.
       Здесь стояла своя обведённая коробочка radius 14 — язык веб-формы.
       Замер iOS 26.5: капсула 38, заливка карточки, без обводки, тень. */
    <div role="search" style={{ marginTop: 14 }}>
      <SearchField
        value={value} onChange={onChange} placeholder={placeholder}
        inputRef={(el) => { ref.current = el; }}
        onClear={() => { onChange(""); ref.current?.focus(); }}
        onKeyDown={(e) => {
          if (e.key === "Escape") onChange("");
          else if (e.key === "Enter") { e.currentTarget.blur(); onSubmit?.(); }
        }} />
    </div>
  );
}

