export const SCRIPTURE_STOP_SET: Set<string> = new Set(STOP.map((s) => s.toLowerCase()));

const escapeRe = (s: string): string => s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");

/* Альтернация всех форм термина, длинное-первым (regex берёт первую
 * подходящую альтернативу — так гарантируем максимальное совпадение). */
const alternation = Array.from(new Set(TERMS.map((t) => t.toLowerCase())))
  .sort((a, b) => b.length - a.length)
  .map(escapeRe)
  .join("|");

/* Без lookbehind (для совместимости с Safari < 16.4) — границы слова
 * проверяются вручную в renderTerms через IS_LETTER. */
export const SCRIPTURE_TERM_REGEX = new RegExp(`(?:${alternation})`, "giu");

