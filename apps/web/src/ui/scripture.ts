const escapeRe = (s: string): string => s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");

/* Альтернация всех форм термина, длинное-первым (regex берёт первую
 * подходящую альтернативу — так гарантируем максимальное совпадение). */
const alternation = Array.from(new Set(TERMS.map((t) => t.toLowerCase())))
  .sort((a, b) => b.length - a.length)
  .map(escapeRe)
  .join("|");

const compoundAlt = Array.from(new Set(COMPOUNDS.map((t) => t.toLowerCase())))
  .sort((a, b) => b.length - a.length)
  .map(escapeRe)
  .join("|");

