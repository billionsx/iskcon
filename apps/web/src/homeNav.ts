/**
 * Детерминированная передача целевого подтаба «Главной» между экранами.
 *
 * Глобальный поиск и deep-link'и ведут на подтаб HomeScreen (Календарь, Центры,
 * Рестораны, Документы). Раньше это держалось на двух хрупких каналах:
 *   1) sessionStorage["home-tab"] — читается при инициализации HomeScreen;
 *   2) событие "home-open" — переключает подтаб у уже смонтированного HomeScreen.
 * Оба зависят от тайминга монтирования/размонтирования (поиск — это replace,
 * HomeScreen пересоздаётся) и от доступности sessionStorage (приватный режим).
 *
 * Этот модуль-синглтон — третий, надёжный канал: значение живёт в памяти бандла,
 * читается синхронно при первом рендере HomeScreen и сразу обнуляется. Им нельзя
 * «промахнуться» по времени и его не сбросит приватный режим. Используем вместе с
 * двумя прежними каналами (пояс + подтяжки).
 */
import type { HomeTabId } from "./HomeTabs";

let pendingTab: HomeTabId | null = null;

/** Запросить подтаб «Главной» к открытию при ближайшем монтировании HomeScreen. */
export function requestHomeTab(tab: HomeTabId): void {
  pendingTab = tab;
}

/** Забрать и обнулить отложенный подтаб (читает HomeScreen при инициализации). */
export function takeHomeTab(): HomeTabId | null {
  const t = pendingTab;
  pendingTab = null;
  return t;
}
