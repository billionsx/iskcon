// ═══════════════════════════════════════════════════════════════════════════
// ЗАКОНЫ ТЕКСТА КАРТОЧЕК — вшиты в код, а не в память. Любой текст личности,
// показываемый в карточке (МКСК, превью и т.п.), ОБЯЗАН пройти через cleanCardText.
// Тем самым нарушения становятся технически невозможны, независимо от данных в БД.
// Канон правил: docs/LAWS_cards.md
//
//   ЗАКОН 1. Никаких точек с запятыми «;» в карточках. «A; b» → «A. B».
//   ЗАКОН 2. Запрещена голая форма «Шри Чайтанья Махапрабху» (и склонения).
//            До санньясы / в общем случае — «Гауранга Махапрабху».
//            Полная форма «Шри Кришна Чайтанья Махапрабху» (санньяса-лила) НЕ трогается
//            — между «Шри» и «Чайтанья» стоит «Кришна», поэтому regex её не задевает.
// ═══════════════════════════════════════════════════════════════════════════

export function cleanCardText(input: string | null | undefined): string {
  let s = (input || "").replace(/[ \t]+/g, " ").trim();
  if (!s) return "";

  // ── ЗАКОН 1: убрать точки с запятыми ──
  if (s.includes(";")) {
    const parts = s.split(";").map((p) => p.trim()).filter(Boolean);
    s = parts
      .map((p, i) => {
        if (i === 0) return p;
        // клауза с большой буквы (кириллица тоже корректно капитализируется)
        return p.charAt(0).toUpperCase() + p.slice(1);
      })
      .join(". ");
    // схлопнуть возможные «. .» / двойные точки после стыка
    s = s.replace(/\.\s*\./g, ".").replace(/\s{2,}/g, " ").trim();
  }

  // ── ЗАКОН 2: запрещённая голая «Шри Чайтанья Махапрабху» → «Гауранга Махапрабху» ──
  s = s
    .replace(/Шри\s+Чайтанья\s+Махапрабху/g, "Гауранга Махапрабху")
    .replace(/Шри\s+Чайтаньи\s+Махапрабху/g, "Гауранги Махапрабху")
    .replace(/Шри\s+Чайтанье\s+Махапрабху/g, "Гауранге Махапрабху")
    .replace(/Шри\s+Чайтаньей\s+Махапрабху/g, "Гаурангой Махапрабху")
    .replace(/Шри\s+Чайтаньею\s+Махапрабху/g, "Гаурангою Махапрабху");

  return s;
}

/**
 * ЗАКОН ЗКН-Т002 — единая точка прогона прозы карточки.
 * Любой текст ЛИЧНОСТИ в любой карточке идёт через cleanCardText.
 *
 * ВАЖНО (ЗКН-БТ004): чужой голос НЕ редактируется. Текст цитат (q.t), стихов и
 * выдержек Прабхупады через этот фильтр НЕ пропускается — даже если внутри стоит
 * неканоничная форма имени. Цитаты рендерятся напрямую (renderSanskrit).
 */
export function cleanCardFields<T extends Record<string, unknown>>(
  obj: T,
  fields: readonly (keyof T)[],
): T {
  const out = { ...obj };
  for (const f of fields) {
    const v = out[f];
    if (typeof v === "string") (out as Record<string, unknown>)[f as string] = cleanCardText(v);
    else if (Array.isArray(v)) {
      (out as Record<string, unknown>)[f as string] = v.map((x) =>
        typeof x === "string" ? cleanCardText(x) : x,
      );
    }
  }
  return out;
}
