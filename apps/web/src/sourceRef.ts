/**
 * ЗКН-БТ001 · ЗКН-БТ006 — ИСТОЧНИК ЛИЧНОСТИ ВИДЕН ЧЕЛОВЕКУ.
 *
 * ПОЧЕМУ ЭТОТ МОДУЛЬ СУЩЕСТВУЕТ.
 *
 * 450 карточек личностей из 728 (62%) не показывали НИ ОДНОЙ ссылки на писание.
 * Карточка утверждала: «Преданный спутник Гауранги Махапрабху» — и молчала,
 * откуда это известно. Ядро обещания проекта — «нет источника, нет утверждения» —
 * нарушалось на большинстве карточек.
 *
 * При этом источник БЫЛ В БАЗЕ. Поле `source_ref` содержало «ГГД 193» — прямую
 * ссылку на стих «Гаура-ганоддеша-дипики». Оно приходило в карточку и НЕ рисовалось.
 *
 * Этот модуль приводит сырую пометку к человеческой форме (ЗКН-БТ006: ссылка
 * пишется ПОЛНОСТЬЮ — название · раздел · глава · стих) и отдаёт её карточке.
 *
 *   «ГГД 193»            → «Гаура-ганоддеша-дипика, стих 193»
 *   «Ч.-ч. Мадхья 20.186» → «Шри Чайтанья-чаритамрита, Мадхья-лила, глава 20, стих 186»
 *   «ЧЧ, Антья 6»         → «Шри Чайтанья-чаритамрита, Антья-лила, глава 6»
 */

/* ⚠️ КИРИЛЛИЦА И `\b` — ЛОВУШКА (ЗКН-Ф011).
 *
 * В JavaScript `\b` определяется через `\w` = [A-Za-z0-9_]. КИРИЛЛИЦА ТУДА НЕ ВХОДИТ.
 * Поэтому /^ГГД\b/ НЕ совпадает с «ГГД 193»: после «Д» (не-\w) и перед пробелом
 * (не-\w) границы слова нет. Первая версия этого модуля молча не срабатывала
 * НИ НА ОДНОЙ ссылке — а тесты «проходили», потому что функция возвращала вход.
 *
 * Вместо `\b` — явный просмотр вперёд: конец строки или НЕ-буква.
 */
const EDGE = "(?![А-Яа-яЁёA-Za-z])";

/** Каноническое название книги по сокращению (ЗКН-И001: имена не сокращаем). */
const BOOKS: Array<[RegExp, string]> = [
  [new RegExp("^ГГД" + EDGE, "i"), "Гаура-ганоддеша-дипика"],
  [new RegExp("^(?:Ч\\.?-?ч\\.?|ЧЧ)" + EDGE, "i"), "Шри Чайтанья-чаритамрита"],
  [new RegExp("^(?:Ч\\.?-?б\\.?|ЧБ)" + EDGE, "i"), "Шри Чайтанья-бхагавата"],
  [new RegExp("^(?:ШБ|Бхаг)" + EDGE, "i"), "Шримад-Бхагаватам"],
  [new RegExp("^БГ" + EDGE, "i"), "Бхагавад-гита как она есть"],
  [new RegExp("^БРС" + EDGE, "i"), "Бхакти-расамрита-синдху"],
  [new RegExp("^БР" + EDGE, "i"), "Бхакти-ратнакара"],
];

/** Раздел (лила/кханда) по сокращению. */
const DIVISIONS: Array<[RegExp, string]> = [
  [new RegExp("Ади" + EDGE, "i"), "Ади-лила"],
  [new RegExp("Мадхья" + EDGE, "i"), "Мадхья-лила"],
  [new RegExp("Антья" + EDGE, "i"), "Антья-лила"],
  [new RegExp("Песн[ья]" + EDGE, "i"), "Песнь"],
];

/**
 * Человеческая форма ссылки на источник.
 * Возвращает null, если ссылка не распознана — тогда показываем как есть,
 * но НИКОГДА не выдумываем (ЗКН-БТ001).
 */
export function formatSourceRef(raw: string | null | undefined): string | null {
  const s = (raw || "").trim();
  if (!s) return null;

  // GBC — не стих, а реестр общества. Даём как есть, по-человечески.
  if (/^GBC/i.test(s)) return "Реестр гуру ИСККОН (GBC)";

  const book = BOOKS.find(([rx]) => rx.test(s));
  if (!book) {
    // Не сокращение, а полное имя источника («Махабхарата», «Рамаяна») — так и оставляем.
    return s;
  }

  const [, title] = book;
  const rest = s.replace(book[0], "").replace(/^[\s,.:·-]+/, "");

  // раздел (Ади/Мадхья/Антья)
  const div = DIVISIONS.find(([rx]) => rx.test(rest));
  const divName = div ? div[1] : null;
  const afterDiv = div ? rest.replace(div[0], "").replace(/^[\s,.:·-]+/, "") : rest;

  const parts: string[] = [title];
  if (divName) parts.push(divName);

  // У «Шримад-Бхагаватам» ТРИ уровня: Песнь.Глава.Стих (10.14.32).
  // У «Чайтанья-чаритамриты» — лила уже вынесена, остаётся Глава.Стих.
  const isSB = /^(?:ШБ|Бхаг)/i.test(s);
  const m3 = afterDiv.match(/^(\d+)[.\s]+(\d+)[.\s]+(\d+(?:[–-]\d+)?)/);
  const m2 = afterDiv.match(/^(\d+)[.\s]+(\d+(?:[–-]\d+)?)/);
  const m1 = afterDiv.match(/^(\d+)/);

  if (isSB && m3) {
    parts.push("Песнь " + m3[1], "глава " + m3[2], "стих " + m3[3]);
  } else if (isSB && m2) {
    parts.push("Песнь " + m2[1], "глава " + m2[2]);
  } else if (m2) {
    parts.push("глава " + m2[1], "стих " + m2[2]);
  } else if (m1) {
    if (divName) parts.push("глава " + m1[1]);
    // У ГГД нумерация сплошная по СТИХАМ — номер это стих, не глава.
    else parts.push(/^ГГД/i.test(s) ? "стих " + m1[1] : "глава " + m1[1]);
  }

  return parts.join(", ");
}
